#include <unistd.h>

void	ft_putnbr_fd(int n, int fd)
{
	char	str[10];
	int		i;

	i = 0;
	if (n == -2147483648)
	{
		write(fd, "-2147483648", 11);
		return ;
	}
	if (n < 0)
	{
		n *= -1;
		write(fd, "-", 1);
	}
	if (n > 9)
	{
		ft_putnbr_fd(n / 10, fd);
		ft_putnbr_fd(n % 10, fd);
	}
	else
	{
		str[i] = '0' + n;
		write(fd, &str[i], 1);
		i++;
	}
}
